    private void mostrarTabla(String valor){
        refrescarTabla();
        String[] columnNames = {"Correo", "Nick", "Password", "Rol" };
        users = new DefaultTableModel(columnNames, 0); // Setting the column names of the Table Model;
        String sql;
        if(valor.equals("")){
            sql = "SELECT correoUsuario, nickUsuario, passwordUsuario, rolUsuario FROM usuario";
        } else {
            sql = "SELECT correoUsuario, nickUsuario, passwordUsuario, rolUsuario FROM usuario WHERE correoUsuario = '"+ valor +"'";
        }
        
        try {
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
           while(rs.next()){
                String correoUsuario = rs.getString(1);
                String nickUsuario = rs.getString(2);
                String passwordUsuario = rs.getString(3);
                String rolUsuario = String.valueOf(rs.getInt(4));
                
                String[] data = {correoUsuario, nickUsuario, passwordUsuario, rolUsuario}; // Saving one row of data
                
                users.addRow(data); // Adding the row to the Table Model
                
                
            }
           table_users.setModel(users);
        } catch(SQLException ex){
            String err = ex.toString(); // Saving the error to a variable while converting it into a string
            
            JOptionPane.showConfirmDialog(this, err);
        }

    }
    private void refrescarTabla(){
        users.setColumnCount(0);
        users.setRowCount(0);
        table_users.revalidate();
    }
