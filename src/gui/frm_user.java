    private void table_usersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_usersMouseClicked
       // Getting the values of the table, when a row is clicked by the user.
        int SelectedRow = table_users.getSelectedRow();
       
       txt_correo.setText(table_users.getValueAt(SelectedRow, 0).toString());
       txt_usuario.setText(table_users.getValueAt(SelectedRow, 1).toString());
       txtp_password.setText(table_users.getValueAt(SelectedRow, 2).toString());
       
       int rolSelected = Integer.parseInt((String) table_users.getValueAt(SelectedRow, 3));
       switch(rolSelected){
           case 0:
               cmb_rol.setSelectedIndex(1);
               break;
           case 1:
               cmb_rol.setSelectedIndex(2);
               break;
           default:
               cmb_rol.setSelectedIndex(0);
       }
       
    }//GEN-LAST:event_table_usersMouseClicked

